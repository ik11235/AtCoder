package production.ABC267.A

object Main extends App {
  val s = scala.io.StdIn.readLine

  if (s == "Monday") {
    println(5)
  } else if (s == "Tuesday") {
    println(4)

  } else if (s == "Wednesday") {
    println(3)

  } else if (s == "Thursday") {
    println(2)

  } else if (s == "Friday") {
    println(1)

  }
}
